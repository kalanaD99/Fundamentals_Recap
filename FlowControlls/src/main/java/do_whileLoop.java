public class do_whileLoop {
    public static void main(String[] args) {
        //eka parak body ek run wela tama condition ek balanne. condition ek true unath nethath body ek run wenaw
        int i=0;
        do {
            System.out.println("doWhile loop "+i);
            i++;
        }while (i<10);
    }
}
